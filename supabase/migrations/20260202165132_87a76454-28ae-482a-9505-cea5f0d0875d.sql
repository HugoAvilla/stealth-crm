-- TABELA 4: service_items_detailed
-- Itens detalhados de serviço para vendas
-- =====================================================

CREATE TABLE public.service_items_detailed (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  sale_id bigint NOT NULL REFERENCES public.sales(id) ON DELETE CASCADE,
  category text NOT NULL CHECK (category IN ('INSULFILM', 'PPF')),
  product_type_id bigint NOT NULL REFERENCES public.product_types(id),
  region_id bigint NOT NULL REFERENCES public.vehicle_regions(id),
  meters_used numeric NOT NULL DEFAULT 0,
  unit_price numeric NOT NULL,
  total_price numeric NOT NULL,
  notes text,
  created_at timestamptz DEFAULT now(),
  company_id bigint NOT NULL REFERENCES public.companies(id) ON DELETE CASCADE
