-- =====================================================
-- TABELA 3: region_consumption_rules
-- Regras de consumo por região e tamanho
-- =====================================================

CREATE TABLE public.region_consumption_rules (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  category text NOT NULL CHECK (category IN ('INSULFILM', 'PPF')),
  region_id bigint NOT NULL REFERENCES public.vehicle_regions(id) ON DELETE CASCADE,
  vehicle_size text NOT NULL CHECK (vehicle_size IN ('P', 'M', 'G')),
  meters_consumed numeric NOT NULL DEFAULT 0,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now(),
  company_id bigint NOT NULL REFERENCES public.companies(id) ON DELETE CASCADE,
  UNIQUE(region_id, vehicle_size, company_id)
);

-- Índices
CREATE INDEX idx_region_consumption_category ON public.region_consumption_rules(category);
CREATE INDEX idx_region_consumption_region ON public.region_consumption_rules(region_id);
