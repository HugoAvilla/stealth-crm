-- =====================================================
-- TABELA 1: product_types
-- Armazena tipos de produtos INSULFILM e PPF
-- =====================================================

CREATE TABLE public.product_types (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  category text NOT NULL CHECK (category IN ('INSULFILM', 'PPF')),
  brand text NOT NULL,
  name text NOT NULL,
  model text,
  light_transmission text,
  description text,
  unit_price numeric DEFAULT 0,
  cost_per_meter numeric DEFAULT 0,
  is_active boolean DEFAULT true,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now(),
  company_id bigint NOT NULL REFERENCES public.companies(id) ON DELETE CASCADE
);

-- Índices
CREATE INDEX idx_product_types_category ON public.product_types(category);
CREATE INDEX idx_product_types_company ON public.product_types(company_id);

-- RLS
ALTER TABLE public.product_types ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Users can view product_types from their company"
  ON public.product_types FOR SELECT
  USING (company_id = get_user_company_id(auth.uid()));

CREATE POLICY "Users can manage product_types from their company"
  ON public.product_types FOR ALL
  USING (company_id = get_user_company_id(auth.uid()))
  WITH CHECK (company_id = get_user_company_id(auth.uid()));

-- Trigger para updated_at
CREATE TRIGGER update_product_types_updated_at
  BEFORE UPDATE ON public.product_types
  FOR EACH ROW
  EXECUTE FUNCTION public.update_updated_at_column();

